#2133. 타일 채우기
"""
N * 8로 채우기
"""

N = int(input().rstrip())
D = [[0] * 8 for _ in range(31)]
print(D)
D[1][0] = 1; D[1][3] = 1; D[1][6] = 1;
for i in range(2,31):
    D[i][0] = D[i-1][7]
    D[i][1] = D[i-1][6]
    D[i][2] = D[i-1][5]
    D[i][3] = D[i-1][4] + D[i-1][7]
    D[i][4] = D[i-1][3]
    D[i][5] = D[i-1][2]
    D[i][6] = D[i-1][7] + D[i-1][1]
    D[i][7] = D[i-1][0] + D[i-1][3] + D[i-1][6]
print(D[N][7])
    